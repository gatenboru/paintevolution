    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        if(jTextFieldAnchoRedimensionarImagen.getText().toString().equals("")){
            JOptionPane.showMessageDialog(this, "Por favor introduzca un número en "
                + "el ancho.", "Error", JOptionPane.ERROR_MESSAGE);
            
            // Devuelve el foco a jTextAnchoRedimensionarImagen.
            jTextFieldAnchoRedimensionarImagen.requestFocus();
        }else if(jTextFieldAltoRedimensionarImagen.getText().toString().equals("")){
             JOptionPane.showMessageDialog(this, "Por favor introduzca un número en "
                 + "el alto.", "Error", JOptionPane.ERROR_MESSAGE);
           
             // Devuelve el foco a jTextFieldAltoRedimensionarImagen.
             jTextFieldAltoRedimensionarImagen.requestFocus();
        }else if(!esValidoNumeroIntroducido(jTextFieldAnchoRedimensionarImagen.getText().toString())){
             JOptionPane.showMessageDialog(this, "Por favor introduzca correctamente un "
                 + " número al ancho.", "Error", JOptionPane.ERROR_MESSAGE);

             // Setea en blanco jTextAnchoRedimensionarImagen.
             jTextFieldAnchoRedimensionarImagen.setText("");
            
             // Devuelve el foco a jTextAnchoRedimensionarImagen.
             jTextFieldAnchoRedimensionarImagen.requestFocus();
        }else if(!esValidoNumeroIntroducido(jTextFieldAltoRedimensionarImagen.getText().toString())){
             JOptionPane.showMessageDialog(this, "Por favor introduzca correctamente un"
                 + " número al alto.", "Error", JOptionPane.ERROR_MESSAGE);
             
             // Setea en blanco jTextFieldAltoRedimensionarImagen.
             jTextFieldAltoRedimensionarImagen.setText("");
             
             // Devuelve el foco a jTextFieldAltoRedimensionarImagen.
             jTextFieldAltoRedimensionarImagen.requestFocus();
        }else{
             /*
              * Se guarda en las variables de clase anchoImagenRedimensionada y 
              * altoImagenRedimensionada queintrodujo el usuario.
              */
             setAnchoImagenRedimensionada(Integer.parseInt(jTextFieldAnchoRedimensionarImagen.getText().toString()));
             setAltoImagenRedimensionada(Integer.parseInt(jTextFieldAltoRedimensionarImagen.getText().toString()));
             this.setVisible(false);
             setRedimensionarImagen(true);
             repaint();
        }
    }//GEN-LAST:event_jButtonAceptarActionPerformed

