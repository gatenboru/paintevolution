    /**
     * Metodo privado que verifica si es valido el numero introducido.
     */
    private boolean esValidoNumeroIntroducido(String numero){
        for(int i = 0; i < numero.length(); i++){
            if(!esNumero(numero.charAt(i))){
                return false;
            }
        } 
        return true;
    }
    
    /**
     * Metodo privado que devuelve true o false si es un numero o no.
     * @param numero
     * @return 
     */
    private boolean esNumero(char numero){
        try{
            // Convertir de char a String.
            String numeroString = String.valueOf(numero);

            Integer.parseInt(numeroString);
            return true;
        }catch(NumberFormatException nfe){            
             return false;
        }
    }

